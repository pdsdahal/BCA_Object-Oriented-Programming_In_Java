package com.basicjava.class8;

public class SwitchDemo {

	public static void main(String[] args) {

		int num=5;
		
		switch (num) {

		case 1:
			System.out.println("1");
			break;

		case 2:
			System.out.println("2");
			break;

		case 3:
			System.out.println("3");
			break;

		default:
			System.out.println("No case");
		}
	}
}
