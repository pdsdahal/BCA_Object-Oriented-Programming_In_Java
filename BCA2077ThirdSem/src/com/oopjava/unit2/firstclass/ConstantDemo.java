package com.oopjava.unit2.firstclass;

public class ConstantDemo {

	public static void main(String[] args) {
		
		//to declare constant variable
		
		final int AGE = 18;
		int num1 = 10;
		int result = AGE + num1;
		
		
		System.out.println("the sum of "+AGE + " + "+num1 + " = "+result);
		System.out.println("Age  : "+AGE);
		
	}
}
