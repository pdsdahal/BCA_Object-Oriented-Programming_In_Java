package com.oopjava.unit2.sixthclass.classwork;

public class DisplayPattern {

	public static void main(String[] args) {

		for (int i = 0; i < 4; i++) {

			for (int j = 0; j <= i; j++) {
				System.out.print(" * ");
			}
			System.out.println("");
		}

		for (int i = 0; i < 4; i++) {

			for (int j = 0; j <= i; j++) {
				System.out.print(" * ");
			}
			System.out.println("");
		}

	}

}
